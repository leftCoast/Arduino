typedef struct RLE565 {
  using draw_func = void (*) (int16_t x, int16_t y, uint16_t *buffer, uint16_t width);
  const int16_t _width;
  const int16_t _height;
  const uint8_t bytes_per_pixel;
  const uint8_t rle_pixel_data[];
  const int16_t width() { return pgm_read_word_near(&_width); }
  const int16_t height() { return pgm_read_word_near(&_height); }
  const uint8_t *rle_ptr() { return rle_pixel_data; }

  void plot(draw_func draw_func_ptr, int16_t xoffset, int16_t yoffset) {
    const int16_t xmax = width();
    const int16_t ymax = height();
    const uint8_t *rle = rle_ptr();
    uint16_t buffer[xmax], xpos = 0, ypos = 0, val = 0;
    uint8_t length = 0;

    while (ypos < ymax) {
      // read more RLE data?
      if ((length & 0x7f) == 0) {
        length = pgm_read_byte_near(rle++);
        if (length & 128) {
          val = pgm_read_word_near(rle);
          rle += 2;
        }
      }

      // constrain to the length of the buffer
      uint8_t plotwidth = length & 0x7f;
      if (xpos + plotwidth > xmax)
        plotwidth -= xpos + plotwidth - xmax;
      length -= plotwidth;

      // which type of RLE data is this?
      if (length & 128) {
        // a run of constant values
        do {
          buffer[xpos++] = val;
        } while (--plotwidth);
      } else {
        // a run of raw data to be copied
        memcpy_P(&buffer[xpos], rle, plotwidth * 2);
        rle += plotwidth * 2;
        xpos += plotwidth;
      }

      // end of buffer reached? plot to screen and reset buffer
      if (xpos == xmax) {
        if (ypos + yoffset >= 0) {
          draw_func_ptr(xoffset, ypos + yoffset, buffer, xmax);
        }
        xpos = 0;
        if (++ypos >= ILI9341_TFTHEIGHT)
          return;
      }
    }
  }
} RLE565;


